test_that("Fine if one matching", {
          fakeFileStrings <- c('Hello = world','SURE\t= indeed','Hello = you')
          newFake <- replace_tag(fakeFileStrings,'SURE','me')
          expect_equal(length(newFake), length(fakeFileStrings))
          expect_equal(length(grep('SURE',newFake)), 1)
          expect_equal(length(grep('me',newFake)), 1)
})
test_that("Test warning if multiple lines matching the tag", {
          fakeFileStrings <- c('Hello = world','SURE\t= indeed','Hello = you')
          expect_warning(replace_tag(fakeFileStrings,'Hello','me'))
})
test_that("Add a line if no matching tag", {
          fakeFileStrings <- c('Hello = world','SURE\t= indeed','Hello = you')
          newFake <- replace_tag(fakeFileStrings,'Bouh','frightened?')
          expect_equal(length(newFake), length(fakeFileStrings)+1)
          expect_equal(length(grep('Bouh',newFake)), 1)
          expect_equal(length(grep('frightened?',newFake)), 1) 
})
test_that("replace_tag fine on dcf", {
          fakeFileStrings <- c('Hello: world','SURE: indeed','Hello: you')
          newFake <- replace_tag(fakeFileStrings,'Bouh','frightened?',sep=": ")
          expect_equal(length(newFake), length(fakeFileStrings)+1)
          expect_equal(length(grep('Bouh',newFake)), 1)
          expect_equal(length(grep('frightened?',newFake)), 1) 
          newFake <- replace_tag(fakeFileStrings,'SURE','me',sep=": ")
          expect_equal(length(newFake), length(fakeFileStrings))
          expect_equal(length(grep('SURE',newFake)), 1)
          expect_equal(length(grep('me',newFake)), 1)
          fakeFileStrings <- c('Hello: world','SURE: indeed','Hello: you','HelloNote: them')
          expect_warning(replace_tag(fakeFileStrings,'Hello','me',sep=": "))
          fakeFileStrings <- c('Hello: world','SURE: indeed','HelloNote: them')
          newFake <- replace_tag(fakeFileStrings,'Hello','me',sep=": ")
          expect_equal(length(newFake), length(fakeFileStrings))
          expect_equal(length(grep('Hello.*me',newFake)), 1)
})
